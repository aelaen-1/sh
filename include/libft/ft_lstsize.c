#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	unsigned int	i;

	if (lst)
	{
		i = 0;
		while (lst)
		{
			lst = lst->next;
			i++;
		}
		return (i);
	}
	return (0);
}
